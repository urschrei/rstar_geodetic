// --- properties ---

/// MINDIST containment: every sampled point of the arc embeds
/// inside its box. Boolean containment, no slack beyond the implementation's margin.
#[hegel::test(test_cases = 200)]
fn prop_arc_box_contains_arc(tc: TestCase) {
    let (a, b) = draw_arc(&tc);
    let bbox = arc_bounding_box(a.to_unit_vector(), b.to_unit_vector());
    for v in sample_arc(a, b, 256) {
        assert!(
            bbox.contains_point(&v),
            "box does not contain arc sample; a=({},{}) b=({},{})",
            a.lon,
            a.lat,
            b.lon,
            b.lat
        );
    }
}

/// The near-antipodal counterpart: the thin shell `draw_arc` misses, with many
/// renormalised samples and zero slack. Catches the u,v-frame undercoverage regime.
#[hegel::test(test_cases = 200)]
fn prop_arc_box_contains_arc_near_180(tc: TestCase) {
    let (a, b) = draw_arc_near_180(&tc);
    let bbox = arc_bounding_box(a.to_unit_vector(), b.to_unit_vector());
    for v in sample_arc(a, b, 1024) {
        assert!(
            bbox.contains_point(&v),
            "near-180 box does not contain arc sample; a=({},{}) b=({},{})",
            a.lon,
            a.lat,
            b.lon,
            b.lat
        );
    }
}

/// Tightness (backs MINMAXDIST): the box matches the AABB of a
/// dense arc sample within `TIGHT_TOL` per face, so no face is grossly over-inflated.
#[hegel::test(test_cases = 200)]
fn prop_arc_box_is_tight(tc: TestCase) {
    const TIGHT_TOL: f64 = 1e-6;
    let (a, b) = draw_arc(&tc);
    let bbox = arc_bounding_box(a.to_unit_vector(), b.to_unit_vector());
    let mut lo = [f64::INFINITY; 3];
    let mut hi = [f64::NEG_INFINITY; 3];
    for v in sample_arc(a, b, 2048) {
        for i in 0..3 {
            lo[i] = lo[i].min(v.0[i]);
            hi[i] = hi[i].max(v.0[i]);
        }
    }
    for i in 0..3 {
        assert!(
            (bbox.lower().0[i] - lo[i]).abs() <= TIGHT_TOL,
            "lower face {i} not tight: box {} vs sample {}; a=({},{}) b=({},{})",
            bbox.lower().0[i],
            lo[i],
            a.lon,
            a.lat,
            b.lon,
            b.lat
        );
        assert!(
            (bbox.upper().0[i] - hi[i]).abs() <= TIGHT_TOL,
            "upper face {i} not tight: box {} vs sample {}; a=({},{}) b=({},{})",
            bbox.upper().0[i],
            hi[i],
            a.lon,
            a.lat,
            b.lon,
            b.lat
        );
    }
}

/// Point-to-arc squared chord matches an independent ternary-search oracle, including
/// queries biased toward the arc's pole-of-circle (the
/// regime that exposes the projection cancellation the analytic form guards against).
#[hegel::test(test_cases = 5000)]
fn prop_point_to_geometry_matches_oracle(tc: TestCase) {
    let (a, b) = draw_arc(&tc);
    let q = draw_query_for_arc(&tc, a, b);
    let c2 = arc_distance_2(a.to_unit_vector(), b.to_unit_vector(), q.to_unit_vector());
    let expected = nearest_arc_c2(q, a, b);
    // Squared-chord tolerance: ~1e-7 sits four orders below the ~1e-3 c2 error a
    // nearest-point bug would produce and four orders above the analytic/ternary
    // numerical noise (~1e-11), so it catches the former without flagging the latter.
    let tol = 1e-7 + expected * 1e-9;
    assert!(
        (c2 - expected).abs() <= tol,
        "arc c2 {c2} != oracle {expected}; a=({},{}) b=({},{}) q=({},{})",
        a.lon,
        a.lat,
        b.lon,
        b.lat,
        q.lon,
        q.lat
    );
}

