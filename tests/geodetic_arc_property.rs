//! Property tests for the great-circle arc primitives (`arc_bounding_box`,
//! `arc_contains_point`, `arc_distance_2`), independent of any tree or leaf type.
//!
//! The shared generators, the ternary-search arc oracle (`nearest_arc_c2`), and the
//! slerp sampler live in the [`geodetic_common`] module; `ARC_GOLDEN` below anchors both
//! the implementation and that oracle to an external high-precision reference.

mod geodetic_common;
use geodetic_common::{
    coord, draw_arc, draw_arc_near_180, draw_query_for_arc, nearest_arc_c2, sample_arc,
};

use hegel::TestCase;
