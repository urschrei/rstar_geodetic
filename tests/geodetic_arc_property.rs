/// Validate both the implementation (`arc_distance_2`) and the test oracle
/// (`nearest_arc_c2`) against the external [`ARC_GOLDEN`] reference.
#[test]
fn arc_distance_and_oracle_match_external_reference() {
    for &(al, aa, bl, bb, ql, qa, gold) in ARC_GOLDEN {
        let (a, b, q) = (coord(al, aa), coord(bl, bb), coord(ql, qa));
        let implementation =
            arc_distance_2(a.to_unit_vector(), b.to_unit_vector(), q.to_unit_vector());
        let oracle = nearest_arc_c2(q, a, b);
        // The whole table matches to <= 1.33e-15 (f64 embedding-rounding floor); 1e-12
        // is comfortably above that and ~1e9 below the c2 error a real nearest-point bug
        // would produce, so it stays a meaningful anchor even for the ~3e-12 case.
        let tol = 1e-12 + gold * 1e-12;
        assert!(
            (implementation - gold).abs() <= tol,
            "impl c2 {implementation} != golden {gold}; a=({al},{aa}) b=({bl},{bb}) q=({ql},{qa})"
        );
        assert!(
            (oracle - gold).abs() <= tol,
            "oracle c2 {oracle} != golden {gold}; a=({al},{aa}) b=({bl},{bb}) q=({ql},{qa})"
        );
    }
}

/// Long-running soak that hunts for new point-to-arc counterexamples to promote into
/// [`ARC_GOLDEN`]. Ignored by default – a full run takes hours. Run it locally with
///
/// ```text
/// cargo nextest run -p rstar --features geodetic --run-ignored all -E 'test(soak_arc)'
/// ```
///
/// (or `cargo test -p rstar --features geodetic -- --ignored soak_arc`). The seed is fresh
/// on each local run (CI derandomises, local does not), so repeated runs explore new
/// inputs. At roughly a millisecond per case (Hegel's per-example cost dominates the
/// maths), 20 million cases is several hours; scale `test_cases` to taste, or re-run to
/// soak longer. On a disagreement Hegel shrinks and prints the minimal `(a, b, q)`; obtain
/// its ground-truth squared chord from `tests/reference/arc_distance_reference.py`, then
/// append the row to [`ARC_GOLDEN`].
///
/// Each case draws either a broad arc or one of the worst-conditioned kinds (near-antipodal
/// or pole-of-circle), so the soak stresses the general space and the hard corners together.
#[ignore = "long-running soak; run locally to find ARC_GOLDEN candidates"]
#[hegel::test(test_cases = 20_000_000)]
fn soak_arc_distance_vs_oracle(tc: TestCase) {
    let near_antipodal = tc.draw(generators::booleans());
    let (a, b) = if near_antipodal {
        draw_arc_near_180(&tc)
    } else {
        draw_arc(&tc)
    };
    let q = draw_query_for_arc(&tc, a, b);
    let c2 = arc_distance_2(a.to_unit_vector(), b.to_unit_vector(), q.to_unit_vector());
    let expected = nearest_arc_c2(q, a, b);
    let tol = 1e-7 + expected * 1e-9;
    let delta = (c2 - expected).abs();
    // Steer the search toward the largest disagreement instead of relying on uniform
    // exploration alone: target how close each case sits to its tolerance, labelled per
    // regime so the antipodal and broad branches are optimised independently rather than one
    // dominating. Normalising by tol keeps the regimes comparable (raw delta would
    // over-weight the near-antipodal c2 ~ 4 regime).
    tc.target_labelled(
        delta / tol,
        if near_antipodal { "antipodal" } else { "broad" },
    );
    assert!(
        delta <= tol,
        "arc_distance_2 and the oracle disagree by {delta} (tol {tol}): \
         impl {c2}, oracle {expected}.\nPromote it: add this case to CURATED in \
         tests/reference/arc_distance_reference.py, run that script, and append the \
         printed row to ARC_GOLDEN:\n    ({}, {}, {}, {}, {}, {}),",
        a.lon,
        a.lat,
        b.lon,
        b.lat,
        q.lon,
        q.lat
    );
}
